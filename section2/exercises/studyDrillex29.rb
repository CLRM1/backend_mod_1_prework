# The if enables the code under it, when the comparison is true.
# For example
apples = 10
oranges = 20

if apples <= oranges
  # Indenting is not necessary for the program to run but it makes the code more readable.
  puts "If we we have #{apples} apples and #{oranges} oranges."
  puts "Then we have more oranges 🍊 than apples 🍎 and our if statement is true."
end
